#!/bin/sh
# AEGIS Installer Script
# Version: 0.0.1 Alpha Test
# Installs AEGIS validation scripts, hooks, and YAML context

set -eu

RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[0;33m'
BLUE='\033[0;34m'
NC='\033[0m'

if [ -z "${TERM:-}" ] || [ "$TERM" = "dumb" ]; then
    RED='' GREEN='' YELLOW='' BLUE='' NC=''
fi

INSTALL_DIR="${AEGIS_INSTALL_DIR:-.}"
TARGET_DIR="${1:-.}"
INSTALL_YAML="${AEGIS_INSTALL_YAML:-yes}"

log_info() { printf '%s[INFO]%s %s\n' "$BLUE" "$NC" "$1"; }
log_success() { printf '%s[OK]%s %s\n' "$GREEN" "$NC" "$1"; }
log_warn() { printf '%s[WARN]%s %s\n' "$YELLOW" "$NC" "$1"; }
log_error() { printf '%s[ERROR]%s %s\n' "$RED" "$NC" "$1"; exit 1; }

echo "=========================================="
echo " AEGIS Installer v2.0"
echo " Target: $TARGET_DIR"
echo "=========================================="
echo ""

# Check if AEGIS scripts exist
if [ ! -d "$INSTALL_DIR/bin" ]; then
    log_error "AEGIS scripts not found at $INSTALL_DIR/bin"
fi

# Create .git/hooks if not exists
if [ -d "$TARGET_DIR/.git" ]; then
    mkdir -p "$TARGET_DIR/.git/hooks"
    
    log_info "Installing pre-commit hook..."
    ln -sf "../../$INSTALL_DIR/bin/PreCommit_Hook.sh" "$TARGET_DIR/.git/hooks/pre-commit"
    chmod +x "$TARGET_DIR/.git/hooks/pre-commit"
    log_success "Pre-commit hook installed"
    
    log_info "Installing commit-msg hook..."
    ln -sf "../../$INSTALL_DIR/bin/Validation_Gates.sh" "$TARGET_DIR/.git/hooks/commit-msg"
    log_success "Commit-msg hook installed"
else
    log_warn "Not a git repository, skipping hooks"
fi

# Create SCRIPTS directory in target
mkdir -p "$TARGET_DIR/.aegis"
log_info "Creating .aegis directory..."
ln -sf "../../$INSTALL_DIR/bin/"* "$TARGET_DIR/.aegis/"
log_success "Scripts linked to .aegis/"

# Install YAML context files
if [ "$INSTALL_YAML" = "yes" ] && [ -d "$INSTALL_DIR/knowledge/yaml" ]; then
    mkdir -p "$TARGET_DIR/knowledge/yaml"
    log_info "Installing YAML context files..."
    
    # Copy YAML files maintaining directory structure
    find "$INSTALL_DIR/knowledge/yaml" -name "*.yml" -type f | while read -r yml_file; do
        relative_path="${yml_file#$INSTALL_DIR/knowledge/yaml/}"
        target_file="$TARGET_DIR/knowledge/yaml/$relative_path"
        target_dir="$(dirname "$target_file")"
        
        mkdir -p "$target_dir"
        cp "$yml_file" "$target_file"
    done
    
    log_success "YAML context files installed to knowledge/yaml/"
else
    log_warn "Skipping YAML installation (set AEGIS_INSTALL_YAML=yes to install)"
fi

# Create validation wrapper
cat > "$TARGET_DIR/validate.sh" << 'WRAPPER'
#!/bin/sh
# AEGIS Validation Wrapper
# Generated by AEGIS Installer

set -eu

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
AEGIS_DIR="$SCRIPT_DIR/.aegis"

"$AEGIS_DIR/Validation_Gates.sh" "$SCRIPT_DIR"
WRAPPER

chmod +x "$TARGET_DIR/validate.sh"
log_success "Validation wrapper created: validate.sh"

echo ""
echo "=========================================="
echo " Installation Complete!"
echo "=========================================="
echo ""
echo "Available commands:"
echo "  ./validate.sh              - Run validation"
echo "  ./.aegis/Validation_Gates.sh - Run validation"
echo "  ./.aegis/Hostile_Env_Test.sh - Test hostile env"
echo "  ./.aegis/Dependency_Check.sh  - Check dependencies"
echo ""
echo "Git hooks installed:"
echo "  .git/hooks/pre-commit  - Pre-commit validation"
echo "  .git/hooks/commit-msg  - Commit message validation"
echo ""
if [ "$INSTALL_YAML" = "yes" ] && [ -d "$INSTALL_DIR/knowledge/yaml" ]; then
    echo "YAML context files:"
    echo "  ./knowledge/yaml/ - AI agent context files"
fi
echo ""
